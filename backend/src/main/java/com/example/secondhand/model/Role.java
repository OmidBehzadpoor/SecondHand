package com.example.secondhand.model;

/**
 * <h2>Role</h2>
 * <p>
 * شمارشی (Enum) نماینده‌ی نقش دسترسی یک {@link User} در سامانه، که در
 * تصمیم‌گیری‌های امنیتی (مانند {@code @PreAuthorize("hasRole('ADMIN')")})
 * استفاده می‌شود.
 * </p>
 *
 * @author تیم بک‌اند
 */
public enum Role {
    /** کاربر با دسترسی کامل مدیریتی (تایید/رد آگهی، مدیریت کاربران و دسته‌بندی‌ها). */
    ADMIN,
    /** کاربر عادی با دسترسی به عملیات استاندارد (ثبت آگهی، چت، علاقه‌مندی، امتیازدهی). */
    USER
}
